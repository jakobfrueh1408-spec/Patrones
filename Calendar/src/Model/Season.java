package Model;

/**
 * The Season enum defines the possible starting periods for a {@link Calendar}.
 * It is used to determine the initiation date of an Erasmus stay, typically
 * aligned with standard European academic semesters.
 */
public enum Season {
    /** * Represents the fall semester, typically initiating on September 1st.
     */
    Autumn,

    /** * Represents the spring semester, typically initiating on April 1st.
     */
    Spring;
}